                                    <a href="https://en.wikipedia.org/wiki/Distributed_ledger" target="_blank">distributed ledger</a>
                                     for transactions. It is used to pay for gas, a unit of computation used in transactions 
                                    and other state transitions. Mistakenly, this currency is also referred to as Ethereum.
                                </p>
                                <p>
                                   It is listed under the ticker symbol ETH and traded on cryptocurrency exchanges, and the Greek 
