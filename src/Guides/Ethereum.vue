                                <h3 class="mt-3">Smart contracts</h3>
                                <p class="mt-3">
                                    Ethereum's 
                                    <a href="https://en.wikipedia.org/wiki/Smart_contract" target="_blank">smart contracts</a>
                                    are based on different computer languages, which developers use 
                                    to program their own functionalities. Smart contracts are high-level programming abstractions 
                                    that are compiled down to EVM bytecode and deployed to the Ethereum blockchain for execution. 
                                    They can be written in Solidity (a language library with similarities to C and JavaScript), 
                                    Serpent (similar to Python, but deprecated), LLL (a low-level Lisp-like language), and Mutan 
                                    (Go-based, but deprecated). There is also a research-oriented language under development called 
                                    Vyper (a strongly-typed Python-derived decidable language).
                                </p>
                                <p>
                                    Smart contracts can be public, which opens up the possibility to prove functionality, e.g. 
                                    self-contained 
                                    <a href="https://en.wikipedia.org/wiki/Provably_fair" target="_blank">provably fair casinos</a>
                                </p>
                                <p>
                                    One issue related to using smart contracts on a public blockchain is that bugs, including 
                                    security holes, are visible to all but cannot be fixed quickly. One example of this is 
                                    the 17 June 2016 attack on The DAO, which could not be quickly stopped or reversed.
                                </p>
                                <p>
                                    There is ongoing research on how to use formal verification to express and prove non-trivial 
                                    properties. A Microsoft Research report noted that writing solid smart contracts can be 
                                    extremely difficult in practice, using The DAO hack to illustrate this problem. 
                                    The report discussed tools that Microsoft had developed for verifying contracts, 
                                    and noted that a large-scale analysis of published contracts is likely to uncover 
                                    widespread vulnerabilities. The report also stated that it is possible to verify the 
                                    equivalence of a Solidity program and the EVM code.
                                </p>
