                                <h3>Characteristics</h3>
                                <p class="mt-2">
                                    As with other cryptocurrencies, the validity of each ether is provided by a 
                                    <a href="https://en.wikipedia.org/wiki/Blockchain" target="_blank">blockchain</a>, which is a 
                                    continuously growing list of records, called blocks, which are linked and secured using 
                                    <a href="https://en.wikipedia.org/wiki/Cryptography" target="_blank">cryptography</a>.
                                    By design, the blockchain is inherently resistant to modification of the data. It is an open, distributed 
                                    ledger that records transactions between two parties efficiently and in a verifiable and permanent way.
                                    Unlike Bitcoin, Ethereum operates using accounts and balances in a manner called state transitions. This does 
                                    not rely upon unspent transaction outputs (UTXOs). State denotes the current balances of all accounts and extra data. 
                                    State is not stored on the blockchain, it is stored in a separate 
                                    <a href="https://en.wikipedia.org/wiki/Merkle_tree" target="_blank">Merkle Patricia tree</a>. A 
                                    <a href="https://en.wikipedia.org/wiki/Cryptocurrency_wallet" target="_blank">cryptocurrency wallet</a> 
                                    stores the 
                                    <a href="https://en.wikipedia.org/wiki/Public-key_cryptography" target="_blank">public and private "keys"</a> or "addresses" which can be used to receive or spend ether. These can be 
                                    generated through BIP 39 style mnemonics for a BIP 32 "HD Wallet". In Ethereum, this is unnecessary as it does 
                                    not operate in a UTXO scheme. With the private key, it is possible to write in the blockchain, effectively 
                                    making an ether transaction.
                                </p>
                                <p>
                                    To send ether to an account, you need the 
                                    <a href="https://en.wikipedia.org/wiki/SHA-3" target="_blank">Keccak-256 hash</a> of the public 
                                    key of that account. Ether accounts are pseudonymous in that they are not linked to individual persons, 
                                    but rather to one or more specific addresses.
                                </p>
