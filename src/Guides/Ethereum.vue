                                </p>
                                <h3>Addresses</h3>
                                <p class="mt-2">
                                    Ethereum addresses are composed of the prefix "0x", a common identifier for 
                                    <a href="https://en.wikipedia.org/wiki/Hexadecimal" target="_blank">hexadecimal</a>, 
                                    concatenated with the rightmost 20 bytes of the Keccak-256 hash 
                                    (<a href="https://en.wikipedia.org/wiki/Endianness" target="_blank">big endian</a>) of the 
                                    <a href="https://en.wikipedia.org/wiki/Elliptic_Curve_Digital_Signature_Algorithm" target="_blank">ECDSA</a> 
                                    public key (the curve used is the so called secp256k1, the same as Bitcoin). In hexadecimal, 
                                    2 digits represents a byte, meaning addresses contain 40 hexadecimal digits. An example of an 
                                    Ethereum address is 0xb794F5eA0ba39494cE839613fffBA74279579268. Contract addresses are in the 
                                    same format, however they are determined by sender and creation transaction nonce. User accounts 
                                    are indistinguishable from contract accounts given only an address for each and no blockchain data. 
                                    Any valid Keccak-256 hash put into the described format is valid, even if it does not correspond 
                                    to an account with a private key or a contract. This is unlike Bitcoin, which uses 
                                    <a href="https://en.wikipedia.org/wiki/Base58base58check" target="_blank">base58check</a>
                                    to ensure that addresses are properly typed.
                                </p>
                                <h3>Comparison to bitcoin</h3>
                                <p class="mt-3">
                                    Ethereum is different from Bitcoin (the cryptocurrency with the largest market 
                                    capitalization as of June 2018) in several aspects:
                                </p>
                                <ul>
                                    <li>
                                        Its block time is 14 to 15 seconds, compared with 10 minutes for bitcoin.
                                    </li>
                                    <li>
                                        Mining of ether generates new coins at a usually consistent rate, 
                                        occasionally changing during hard forks, while for bitcoin the rate 
                                        halves every 4 years.
                                    </li>
                                    <li>
                                        For proof-of-work, it uses the Ethash algorithm which reduces the advantage 
                                        of specialized ASICs in mining.
                                    </li>
                                    <li>
                                        Transaction fees differ by computational complexity, bandwidth use and 
                                        storage needs (in a system known as gas), while bitcoin transactions compete 
                                        by means of transaction size, in bytes.
                                    </li>
                                    <li>
                                        Ethereum gas units each have a price that can be specified in a transaction. 
                                        This is typically measured in Gwei. Bitcoin transactions usually have fees 
                                        specified in satoshis per byte.
                                    </li>
                                    <li>
                                        Ethereum uses an account system where values in Wei are debited from accounts 
                                        and credited to another, as opposed to Bitcoin's UTXO system, which is more 
                                        analogous to spending cash and receiving change in return.
                                    </li>
                                </ul>
